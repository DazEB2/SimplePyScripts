                if not variant_by_el:
                    raise Exception("–ß—Ç–æ-—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫ - –Ω–µ—Ç –∫–ª–∞—Å—Å–æ–≤ –Ω–∞ –≤—ã–±–æ—Ä –ø—Ä–∏ —Å—Ç–∞—Ä—Ç–µ")

                current_classes = list(variant_by_el.keys())
                print("–ö–ª–∞—Å—Å—ã:", current_classes)

                # –ï—Å–ª–∏ –≤—Å–µ –∫–ª–∞—Å—Å—ã –ø–æ—Å–µ—â–µ–Ω—ã
