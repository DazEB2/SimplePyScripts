                    print(f'–¢–µ–∫—É—â–∞—è –≤–µ—Ç–∫–∞ –∫–ª–∞—Å—Å–æ–≤ –æ—Ç–º–µ—á–µ–Ω–∞ –∫–∞–∫ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω–∞—è, –Ω–æ —Ñ–∏–Ω–∞–ª—å–Ω—ã–π –∫–ª–∞—Å—Å –Ω–µ–∏–∑–≤–µ—Å—Ç–µ–Ω: "{child.get_full_name()}"')
                    button_cancel.click()
