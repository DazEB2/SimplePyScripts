                    child = current_node.get_child([name])

                    # –ï—Å–ª–∏ –Ω–∞—à–ª–∏, —Ç–æ –≤—ã–±–∏—Ä–∞–µ–º —ç—Ç–æ—Ç –∫–ª–∞—Å—Å –≤ UI
